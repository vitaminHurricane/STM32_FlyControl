void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if (htim == &htim3) {
    static uint16_t cnt = 0, cnt1 = 0;
    cnt++, cnt1++;
    current_time++;
    //Debug_Command(command);				//需要串口调试时解除注释
    if (cnt >= 10) {                //10ms进行一次姿态解算
      MPU6050_AttitudeEstimation(MPU6050);
      if (PID_Start) {
        PID_Control();
      }
      cnt = 0;
    }
    if (Emergy_Flag) {
      RX_Buf[0] = Motor_All_OFF;
      H24_Command();
      Delay_ms(700);
      Emergy_Flag = false;
      Bind_Flag = false;
    }
    if (cnt1 >= 100 && throttle >= 150) {
      if (PID_Start) {
        Balance_Fix();
      }
      cnt1 = 0;
    }
  }
}

